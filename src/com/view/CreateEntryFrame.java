    private IEntry getEntry() {

        IEntry entry = new Entry();
        entry.setDate(date);
        entry.setTime((Time) jcomboTimes.getSelectedItem());
        entry.setLastname(jtxtLastName.getText());
        entry.setFirstname(jtxtFirstName.getText());
        entry.setMiddlename(jtxtMiddleName.getText());
        entry.setPhone(jtxtPhone.getText());
        entry.setEmail(jtxtEmail.getText());
        entry.setShoeSize(jtxtShoeSize.getText());
        entry.setProductModel(jcomboProductModel.getSelectedItem().toString());

        return entry;
    }

