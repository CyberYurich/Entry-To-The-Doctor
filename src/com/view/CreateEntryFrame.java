    private Date date;

    @Override
    public void setDate(Date date) {
        this.date = date;
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd.MM.yyyy");
        jlblDate.setText(dateFormat.format(this.date));
    }

    @Override
    public void setTimes(List<Time> timesList) {
        jcomboTimes.removeAllItems();
        for (Time time : timesList) {
            jcomboTimes.addItem(time);
        }
    }

